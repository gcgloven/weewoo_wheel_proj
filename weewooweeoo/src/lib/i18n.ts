/**
 * Language instruction appended to LLM system prompts.
 */
export function languageInstruction(lang: Language): string {
  if (lang === 'zh') return '\n\nIMPORTANT: Respond in Chinese (Simplified). 请用简体中文回答。';
  return '';
}
